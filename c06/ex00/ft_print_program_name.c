#include<unistd.h>

int	main(int ac, char **av)
{
	int	i;

	i = 0;
	ac = 0;
	while (av[ac][i])
	{
		write(1, &av[ac][i], 1);
		i++;
	}
	write(1, "\n", 1);
}
